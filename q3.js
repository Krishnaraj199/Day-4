/* 
1. window:
The window object is the global object in a browser environment.
It represents the browser window and serves as the root of the document object model (DOM).
You can access it directly as window.
Properties and methods related to the browser window are part of the window object.
Examples of properties/methods:
window.innerWidth: Returns the inner width of the browser window.
window.innerHeight: Returns the inner height of the browser window.
window.location: Provides information about the current URL.
window.alert(): Displays an alert dialog.
window.open(): Opens a new browser window or tab.
2. screen:
The screen object provides information about the physical screen dimensions.
It includes properties like screen.width (screen width in pixels) and screen.height (screen height in pixels).
Useful for determining screen resolution and adjusting layouts accordingly.
3. document:
The document object represents the HTML document loaded in the browser.
It is a part of the DOM and allows you to interact with the content on the page.
You can access it as window.document or simply document.
Properties and methods related to the document structure are part of the document object.
Examples of properties/methods:
document.title: Gets or sets the title of the document.
document.getElementById(): Finds an element by its ID.
document.createElement(): Creates a new HTML element.
document.querySelector(): Finds the first matching element based on a CSS selector.
*/